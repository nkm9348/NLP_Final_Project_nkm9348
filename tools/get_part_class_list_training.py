def remove_parenthesized_items(clas_item):
    import re
    left_paren = re.compile('\(')
    right_paren = re.compile('\)')
    left_paren_match = left_paren.search(clas_item)
    while left_paren_match:
        right_paren_match = right_paren.search(clas_item,left_paren_match.start(0))
        if right_paren_match:
            right_item =clas_item[right_paren_match.end(0):]
            clas_item = clas_item[:left_paren_match.start(0)]+right_item
            left_paren_match = left_paren.search(clas_item)
        else:
            clas_item = clas_item[:left_paren_match.start(0)]
            left_paren_match = left_paren.search(clas_item,left_paren_match.end(0))
    return(clas_item)
           
           

def normalize_clas_item(clas_item):
    import re
    clas_item = clas_item.upper()
    clas_item = remove_parenthesized_items(clas_item)
    clas_item = re.sub(' +',' ',clas_item)
    return(clas_item)

