def normalize_partitive_class(feature):
    if feature == 'METONYM':
        return('MERONYM')  ## probably typo
    elif feature == 'INSTANCE':
        return('INSTANCE-OF-SET')
    elif feature.startswith('NOMADJ'):
        return('NOMADJ')
    elif feature.startswith('NOMLIKE'):
        return('NOM')
    elif feature == 'BODY-PART':
        return('PART-OF-BODY-FURNITURE-ETC')
    elif 'feature' in ['BOOK-CHAPTER','BORDER','DIVISION']:
        return('MERONYM')
    else:
        return(feature)

def derive_part_class_list():
    output = []
    for inclass in input_classes:
        class_set = inclass.split('/')
        for clas_item in class_set:
            clas_item =  normalize_partitive_class(clas_item)
            if not clas_item in output:
                output.append(clas_item)
    return(output)

